export interface Lesson {
  /** 课节编号，如 "01" */
  code: string
  /** 概念名 */
  title: string
  /** 锚点副标题：一句话点出这节到底在讲什么 */
  subtitle: string
  /** 展开后显示的本节内容说明 */
  desc: string
  /** 本节配套的可交付小任务/课堂体验：学完这节用它的知识能解决的一件事 */
  task: string
}

export interface CourseLayer {
  /** 单元标签，如 "单元一 · 心智模型：它是什么" */
  layer: string
  en: string
  /** 该单元一句话说明 */
  summary: string
  lessons: Lesson[]
}

